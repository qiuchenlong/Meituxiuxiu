package com.meituxiuxiu.android.jsonbean;

public class AppInfo {

	private Gouwu gouwu;
	private Index index;
	private Koudai koudai;
	private String country;
	public Gouwu getGouwu() {
		return gouwu;
	}
	public void setGouwu(Gouwu gouwu) {
		this.gouwu = gouwu;
	}
	public Index getIndex() {
		return index;
	}
	public void setIndex(Index index) {
		this.index = index;
	}
	public Koudai getKoudai() {
		return koudai;
	}
	public void setKoudai(Koudai koudai) {
		this.koudai = koudai;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	
	
	
}
